var showDetail = function(photo) {
    var t = `
        <img class="show-big-img animated infinite" src="${photo}">
    `
    return t
}

var stopButton = function() {
    var stopB = e('.stop-button')
    bindEvent(stopB, "click", function(){
        var del = e('.show-big-img')
        var className = "animated"
        deleteClass(del, className)
    })
}

var startButton = function() {
    var startB = e('.start-button')
    bindEvent(startB, "click", function(){
        var add = e('.show-big-img')
        var className = "animated"
        addClass(add, className)
    })
}

var insertImg = function(photo) {
    var showDiv = e('.show-oneimg')
    showDiv.innerHTML = ''
    t = showDetail(photo)
    appendHtml(showDiv, t)
}

